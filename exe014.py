''' Escreva um programa que converta uma temperatura digitando em graus 
Celsius e converta para graus Fahrenheit.'''

c=float(input('Digite a temperatura em °C: '))
f=((9*c)/5)+32
print(f'A temperatura {c} corresponde a {f} °F')